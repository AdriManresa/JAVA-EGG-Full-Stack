package Entidades;

/**
 *
 * @author Adri
 */
public class Cancion {
    /*ATRIBUTOS*/
    String titulo;
    String autor;
    
    /*CONSTRUCTOR VACIO*/

    public Cancion() {
    }
    /*CONTRUCTOR*/

    public Cancion(String titulo, String autor) {
        this.titulo = titulo;
        this.autor = autor;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }
    
}
