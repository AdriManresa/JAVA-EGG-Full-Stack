package Entidades;

/**
 *
 * @author Adri
 */
public class Persona {
    
    /*ATRIBUTOS*/
    String nombre;
    String sexo;
    int edad;
    double peso;
    double altura;
    
    /*CONSTRUCTOR VACIO*/

    public Persona() {
    }
    /*CONSTRUCTOR*/

    public Persona(String nombre, String sexo, int edad, int peso, int altura) {
        this.nombre = nombre;
        this.sexo = sexo;
        this.edad = edad;
        this.peso = peso;
        this.altura = altura;
    }

    /*GETTER AND SETTER*/
    
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }
    
}
